package com.example.com.careasysell.options.model;

public class VehicleHeatModel {

    private String carPoster;
    private String carTitle;
    private String putAwayTime;
    private String shareTime;
    private String browsTime;
    private int carRankings;

    public int getCarRankings() {
        return carRankings;
    }

    public void setCarRankings(int carRankings) {
        this.carRankings = carRankings;
    }

    public String getCarPoster() {
        return carPoster;
    }

    public void setCarPoster(String carPoster) {
        this.carPoster = carPoster;
    }

    public String getCarTitle() {
        return carTitle;
    }

    public void setCarTitle(String carTitle) {
        this.carTitle = carTitle;
    }

    public String getPutAwayTime() {
        return putAwayTime;
    }

    public void setPutAwayTime(String putAwayTime) {
        this.putAwayTime = putAwayTime;
    }

    public String getShareTime() {
        return shareTime;
    }

    public void setShareTime(String shareTime) {
        this.shareTime = shareTime;
    }

    public String getBrowsTime() {
        return browsTime;
    }

    public void setBrowsTime(String browsTime) {
        this.browsTime = browsTime;
    }
}
