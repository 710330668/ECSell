package com.example.com.careasysell.market.ui.model;

public class ShareRankModel {
    private int rank;
    private String imgUrl;
    private String name;
    private String shareCount;
    private String browerCount;

    public int getRank() {
        return rank;
    }

    public void setRank(int rank) {
        this.rank = rank;
    }

    public String getImgUrl() {
        return imgUrl;
    }

    public void setImgUrl(String imgUrl) {
        this.imgUrl = imgUrl;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getShareCount() {
        return shareCount;
    }

    public void setShareCount(String shareCount) {
        this.shareCount = shareCount;
    }

    public String getBrowerCount() {
        return browerCount;
    }

    public void setBrowerCount(String browerCount) {
        this.browerCount = browerCount;
    }

}
